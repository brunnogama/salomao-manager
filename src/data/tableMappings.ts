export function getModulesForTable(tableName: string): string[] {
  const files = TABLE_MAPPINGS[tableName] || [];
  const modules = new Set<string>();

  files.forEach(file => {
    const p = file.toLowerCase();
    if (p.includes('collaborator') || p.includes('\\rh') || p.includes('/rh')) modules.add('RH');
    if (p.includes('controladoria')) modules.add('Controladoria');
    if (p.includes('finance') || p.includes('financeiro') || p.includes('reembolso')) modules.add('Financeiro');
    if (p.includes('crm')) modules.add('CRM');
    if (p.includes('operational') || p.includes('operacoes') || p.includes('compras') || p.includes('produtos')) modules.add('Operações');
    if (p.includes('secretaria') || p.includes('familia')) modules.add('Secretaria');
    if (p.includes('aeronave')) modules.add('Aeronave');
    if (p.includes('settings') || p.includes('config')) modules.add('Configurações');
  });

  const mods = Array.from(modules);
  return mods.length > 0 ? mods : ['Geral'];
}

